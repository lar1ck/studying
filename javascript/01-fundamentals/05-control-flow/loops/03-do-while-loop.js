/**
 * 03-do-while-loop.js
 * --------------------
 * This file demonstrates how to use do...while loops in JavaScript.
 * Do...while loops execute the code block at least once before checking the condition.
 */

console.log("Do...While Loop Example:");
let j = 1;
do {
    console.log(`Do...While iteration: ${j}`);
    j++;
} while (j <= 5);
