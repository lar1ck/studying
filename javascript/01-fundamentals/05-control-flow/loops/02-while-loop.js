/**
 * 02-while-loop.js
 * -----------------
 * This file demonstrates how to use while loops in JavaScript.
 * While loops continue to execute as long as their condition remains true.
 */

console.log("While Loop Example:");
let count = 1;
while (count <= 5) {
    console.log(`Count: ${count}`);
    count++;  // Increment count to eventually break the loop.
}
