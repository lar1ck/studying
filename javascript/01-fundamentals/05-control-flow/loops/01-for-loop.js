/**
 * 01-for-loop.js
 * ---------------
 * This file demonstrates how to use for loops in JavaScript.
 * For loops are used when the number of iterations is known.
 */

console.log("For Loop Example:");
for (let i = 1; i <= 5; i++) {
    // Logs the iteration number.
    console.log(`Iteration ${i}`);
}
